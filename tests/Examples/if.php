<?php
// if
if ($foo) {
    $foo = 'bar';
}

// if else
if ($foo) {
    $foo = 'bar';
} else {
    $foo = 'baz';
}

// if elseif
if ($foo) {
    $foo = 'bar';
} elseif ($bar) {
    $foo = 'baz';
}

// if elseif else
if ($foo) {
    $foo = 'bar';
} elseif ($bar) {
    $foo = 'baz';
} else {
    $foo = 'dib';
}

// if elseif elseif else
if ($foo) {
    $foo = 'bar';
} elseif ($bar) {
    $foo = 'baz';
} elseif ($baz) {
    $foo = 'dib';
} else {
    $foo = 'gir';
}
